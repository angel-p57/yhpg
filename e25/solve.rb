def norm a,b
  a*a+b*b
end

def solve input
  # implement
  w,h,x,y=input.scan(/\d+/).map &:to_i
  w-=1
  h-=1
  al=-x
  au=w-x
  bl=-y
  bu=h-y
  bmal=[x-w,y-h].max
  bmau=[x,y].min
  apbl=[x-w,-y].max
  apbu=[x,h-y].min
  cand=[*al..au].product([*bl..bu]).select{|a,b|b-a>=bmal&&b-a<=bmau&&a+b>=apbl&&a+b<=apbu}.sort_by{|a,b|-norm(a,b)}
  return ?- if cand.empty?||norm(*cand[0])==0||cand.size>1&&norm(*cand[0])==norm(*cand[1])
  a,b=cand[0]
  "(%d,%d),(%d,%d),(%d,%d)" % [x+a,y+b,x+a-b,y+a+b,x-a-b,y+a-b]
end

def test(n,input,expected)
  actual=solve(input).to_s
  puts "#{n}: "+(actual==expected ? "ok":"ng ( #{actual} against #{expected} for #{input} )")
end

