def solve input
  # implement
  n=input.to_i
  t,*a=?0+n.to_s(3)
  if n%3==0
    a.push(t.sub(/[^0]0+$/){|m|?0+m[0]*(m.size-1)}.to_i(3)) if n!=0
    a.push(n+1,n+2)
  else
    a.push(n-n%3)
    a.push(t.sub(/0([^0])\1*$/){|m|m[-1]+?0*(m.size-1)}.to_i(3)) if t[/0([^0])\1*$/]
  end
  a*?,
end

def test(n,input,expected)
  actual=solve(input).to_s
  puts "#{n}: "+(actual==expected ? "ok":"ng ( #{actual} against #{expected} for #{input} )")
end

